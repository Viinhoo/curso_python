"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('luiz otavio')
print(1, 'luiz "otavio"')

# Aspas duplas
print("luiz otavio")
print("luiz 'otavio'")

# Escape
print('luiz \'otavio\'')

# r
print(r'luiz \'otavio\'')
