# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou ngativo. int sem sinal é considerado
# positivo.
print(11) # int
print(-11) # int
print(0)

# float -> Número com ponto fluente
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo
print(1.1, 10.11)
print(0.0, -1.5)

#A "função" type mostra o tipo que o python
# inferiu  valor
print(type('otavio'))
print(type(1)) 
print(type(-1))
print(type(0))
print(type(1.1), type(-1.1), type(0.0) ) 


